// ─────────────────────────────────────────────────────────────────────────────
// Topic Report Generator — creates comprehensive Markdown reports from
// evidence data. Pulls together all sources, tiers, and insights for a topic.
// ─────────────────────────────────────────────────────────────────────────────

import type { EvidencePiece, EvidenceTier } from "@/lib/evidence";
import { tierMeta } from "@/lib/evidence";
import type { NormalizedEvidence } from "@/lib/ingestion/types";
import { classifyTierAdvanced } from "@/lib/evidence-classifier";
import { parseAbstract } from "@/lib/abstract-parser-v2";

export interface ReportOptions {
  /** Include evidence quality breakdown */
  includeQualityBreakdown?: boolean;
  /** Include source distribution */
  includeSourceDistribution?: boolean;
  /** Include timeline */
  includeTimeline?: boolean;
  /** Include key findings */
  includeKeyFindings?: boolean;
  /** Include recommendations section */
  includeRecommendations?: boolean;
  /** Max items per section */
  maxItems?: number;
}

const DEFAULT_OPTIONS: ReportOptions = {
  includeQualityBreakdown: true,
  includeSourceDistribution: true,
  includeTimeline: true,
  includeKeyFindings: true,
  includeRecommendations: true,
  maxItems: 20,
};

/**
 * Generate a comprehensive Markdown report for a topic.
 */
export function generateTopicReport(
  topicId: string,
  topicName: string,
  evidence: EvidencePiece[],
  opts: ReportOptions = {},
): string {
  const options = { ...DEFAULT_OPTIONS, ...opts };
  const now = new Date();
  const lines: string[] = [];

  // ── Header ──
  lines.push(`# ${topicName} — Evidence Report`);
  lines.push("");
  lines.push(
    `> Generated by PRAN on ${now.toLocaleDateString("en-US", { year: "numeric", month: "long", day: "numeric" })}`,
  );
  lines.push(`> Total evidence pieces: ${evidence.length}`);
  lines.push("");
  lines.push("---");
  lines.push("");

  // ── Executive Summary ──
  lines.push("## Executive Summary");
  lines.push("");
  lines.push(generateExecutiveSummary(evidence));
  lines.push("");

  // ── Evidence Quality Breakdown ──
  if (options.includeQualityBreakdown) {
    lines.push("## Evidence Quality Distribution");
    lines.push("");
    lines.push(generateQualityBreakdown(evidence));
    lines.push("");
  }

  // ── Source Distribution ──
  if (options.includeSourceDistribution) {
    lines.push("## Source Distribution");
    lines.push("");
    lines.push(generateSourceDistribution(evidence));
    lines.push("");
  }

  // ── Key Findings ──
  if (options.includeKeyFindings) {
    lines.push("## Key Findings");
    lines.push("");
    lines.push(generateKeyFindings(evidence, options.maxItems!));
    lines.push("");
  }

  // ── Timeline ──
  if (options.includeTimeline) {
    lines.push("## Publication Timeline");
    lines.push("");
    lines.push(generateTimeline(evidence));
    lines.push("");
  }

  // ── Recommendations ──
  if (options.includeRecommendations) {
    lines.push("## Recommendations");
    lines.push("");
    lines.push(generateRecommendations(evidence));
    lines.push("");
  }

  // ── Evidence Table ──
  lines.push("## Evidence Table");
  lines.push("");
  lines.push(generateEvidenceTable(evidence, options.maxItems!));
  lines.push("");

  // ── Footer ──
  lines.push("---");
  lines.push("");
  lines.push("*This report was generated by PRAN (Platform for Research Analysis of N-evidence).*");
  lines.push("*Evidence tiers are classified using methodology-aware pattern analysis.*");
  lines.push("*Confidence scores reflect tier quality, recency, and sample size.*");

  return lines.join("\n");
}

/**
 * Generate a report from NormalizedEvidence (from ingestion layer).
 */
export function generateReportFromNormalized(
  topicId: string,
  topicName: string,
  items: NormalizedEvidence[],
  opts: ReportOptions = {},
): string {
  const evidence: EvidencePiece[] = items.map((item) => {
    const parsed = parseAbstract(item.abstract);
    const tier = classifyTierAdvanced(item.abstract || item.title, {
      source: item.sourceName,
    }).tier;

    return {
      id: item.id,
      title: item.title,
      tier,
      year: item.year,
      source: item.sourceName,
      authors: item.authors,
      journal: item.journal,
      n: parsed.sampleSize ?? item.sampleSize,
      effect: parsed.effectSize ?? item.effect,
      confidence: 0,
      url: item.url,
      abstract: item.abstract,
    };
  });

  return generateTopicReport(topicId, topicName, evidence, opts);
}

