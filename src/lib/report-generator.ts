// Section generators
// ─────────────────────────────────────────────────────────────────────────────

function generateExecutiveSummary(evidence: EvidencePiece[]): string {
  if (evidence.length === 0) {
    return "No evidence found for this topic. Consider broadening the search query.";
  }

  const byTier = groupByTier(evidence);
  const bySource = groupBySource(evidence);
  const years = evidence.map((e) => e.year).filter((y): y is number => y !== null);
  const avgConfidence = Math.round(
    evidence.reduce((sum, e) => sum + e.confidence, 0) / evidence.length,
  );

  const highTierCount = (byTier["meta-analysis"]?.length ?? 0) + (byTier["rct"]?.length ?? 0);
  const guidelineCount = byTier["guideline"]?.length ?? 0;

  const parts: string[] = [];

  parts.push(
    `This report synthesizes **${evidence.length} pieces of evidence** from **${Object.keys(bySource).length} sources**.`,
  );

  if (highTierCount > 0) {
    parts.push(
      `**${highTierCount} high-quality studies** (meta-analyses and RCTs) were identified.`,
    );
  }

  if (guidelineCount > 0) {
    parts.push(`**${guidelineCount} clinical guidelines** were found from major organizations.`);
  }

  if (years.length > 0) {
    const minYear = Math.min(...years);
    const maxYear = Math.max(...years);
    parts.push(`Evidence spans from **${minYear}** to **${maxYear}**.`);
  }

  parts.push(`Average confidence score: **${avgConfidence}%**.`);

  return parts.join(" ");
}

function generateQualityBreakdown(evidence: EvidencePiece[]): string {
  const byTier = groupByTier(evidence);
  const total = evidence.length;
  const lines: string[] = [];

  lines.push("| Tier | Count | Percentage | Avg Confidence |");
  lines.push("|------|-------|------------|----------------|");

  const tierOrder: EvidenceTier[] = [
    "meta-analysis",
    "rct",
    "guideline",
    "cohort",
    "case-report",
    "expert-opinion",
  ];

  for (const tier of tierOrder) {
    const items = byTier[tier] ?? [];
    if (items.length === 0) continue;

    const pct = Math.round((items.length / total) * 100);
    const avgConf = Math.round(items.reduce((sum, e) => sum + e.confidence, 0) / items.length);

    lines.push(`| ${tierMeta[tier].label} | ${items.length} | ${pct}% | ${avgConf}% |`);
  }

  return lines.join("\n");
}

function generateSourceDistribution(evidence: EvidencePiece[]): string {
  const bySource = groupBySource(evidence);
  const lines: string[] = [];

  lines.push("| Source | Count | Percentage |");
  lines.push("|--------|-------|------------|");

  const sorted = Object.entries(bySource).sort((a, b) => b[1].length - a[1].length);
  const total = evidence.length;

  for (const [source, items] of sorted) {
    const pct = Math.round((items.length / total) * 100);
    lines.push(`| ${source} | ${items.length} | ${pct}% |`);
  }

  return lines.join("\n");
}

function generateKeyFindings(evidence: EvidencePiece[], maxItems: number): string {
  // Sort by confidence, take top N
  const sorted = [...evidence].sort((a, b) => b.confidence - a.confidence).slice(0, maxItems);

  const lines: string[] = [];

  for (const item of sorted) {
    const tierLabel = tierMeta[item.tier]?.label ?? item.tier;
    const effect = item.effect ? ` — ${item.effect}` : "";
    lines.push(`- **[${tierLabel}]** ${item.title}${effect} (${item.year ?? "n.d."})`);
  }

  return lines.join("\n");
}

function generateTimeline(evidence: EvidencePiece[]): string {
  const byYear = new Map<number, EvidencePiece[]>();

  for (const item of evidence) {
    if (item.year === null) continue;
    const existing = byYear.get(item.year) ?? [];
    existing.push(item);
    byYear.set(item.year, existing);
  }

  if (byYear.size === 0) {
    return "No publication years available for timeline analysis.";
  }

  const sorted = [...byYear.entries()].sort((a, b) => a[0] - b[0]);
  const maxCount = Math.max(...sorted.map(([, items]) => items.length));

  const lines: string[] = [];
  lines.push("```");

  for (const [year, items] of sorted) {
    const barLength = Math.round((items.length / maxCount) * 30);
    const bar = "█".repeat(barLength);
    lines.push(`${year} │ ${bar} (${items.length})`);
  }

  lines.push("```");
  return lines.join("\n");
}

function generateRecommendations(evidence: EvidencePiece[]): string {
  const byTier = groupByTier(evidence);
  const lines: string[] = [];

  const metaCount = byTier["meta-analysis"]?.length ?? 0;
  const rctCount = byTier["rct"]?.length ?? 0;
  const guidelineCount = byTier["guideline"]?.length ?? 0;
  const cohortCount = byTier["cohort"]?.length ?? 0;

  if (metaCount >= 3) {
    lines.push(
      "- **Strong evidence base**: Multiple meta-analyses available. Consider conducting an updated systematic review.",
    );
  } else if (rctCount >= 3) {
    lines.push(
      "- **Moderate evidence base**: Several RCTs available but no meta-analysis. A systematic review would strengthen the evidence.",
    );
  } else if (rctCount + metaCount === 0 && cohortCount > 0) {
    lines.push(
      "- **Limited experimental evidence**: Only observational studies found. Randomized trials are needed to establish causality.",
    );
  } else if (evidence.length === 0) {
    lines.push(
      "- **No evidence found**: Consider broadening the search query or checking alternative data sources.",
    );
  }

  if (guidelineCount > 0) {
    lines.push(
      `- **Clinical guidelines available**: ${guidelineCount} guideline(s) from major organizations should be consulted for clinical decision-making.`,
    );
  }

  const recentEvidence = evidence.filter((e) => e.year && e.year >= new Date().getFullYear() - 3);
  if (recentEvidence.length > 0) {
    lines.push(
      `- **Recent evidence**: ${recentEvidence.length} studies published in the last 3 years.`,
    );
  } else if (evidence.length > 0) {
    lines.push(
      "- **Evidence recency**: Most evidence is older than 3 years. Newer studies may be needed.",
    );
  }

  if (lines.length === 0) {
    lines.push("- Insufficient evidence to provide specific recommendations at this time.");
  }

  return lines.join("\n");
}

function generateEvidenceTable(evidence: EvidencePiece[], maxItems: number): string {
  const sorted = [...evidence].sort((a, b) => b.confidence - a.confidence).slice(0, maxItems);

  const lines: string[] = [];
  lines.push("| # | Tier | Title | Year | Source | N | Confidence |");
  lines.push("|---|------|-------|------|--------|---|------------|");

  for (let i = 0; i < sorted.length; i++) {
    const item = sorted[i];
    const tierLabel = tierMeta[item.tier]?.label ?? item.tier;
    const title = item.title.length > 60 ? item.title.slice(0, 57) + "..." : item.title;
    const n = item.n ? item.n.toLocaleString() : "—";
    lines.push(
      `| ${i + 1} | ${tierLabel} | ${title} | ${item.year ?? "n.d."} | ${item.source} | ${n} | ${item.confidence}% |`,
    );
  }

  if (evidence.length > maxItems) {
    lines.push(
      `\n*Showing top ${maxItems} of ${evidence.length} evidence pieces (sorted by confidence).*`,
    );
  }

  return lines.join("\n");
}

// ─────────────────────────────────────────────────────────────────────────────
// Helpers
// ─────────────────────────────────────────────────────────────────────────────

function groupByTier(evidence: EvidencePiece[]): Record<string, EvidencePiece[]> {
  const groups: Record<string, EvidencePiece[]> = {};
  for (const item of evidence) {
    const key = item.tier;
    if (!groups[key]) groups[key] = [];
    groups[key].push(item);
  }
  return groups;
}

function groupBySource(evidence: EvidencePiece[]): Record<string, EvidencePiece[]> {
  const groups: Record<string, EvidencePiece[]> = {};
  for (const item of evidence) {
    const key = item.source;
    if (!groups[key]) groups[key] = [];
    groups[key].push(item);
  }
  return groups;
}
